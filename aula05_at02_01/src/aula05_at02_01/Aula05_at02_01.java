public class Aula05_at02_01 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int i = 1;
        System.out.println("------------ Inicio While -------------");
        while (i <= 10){
            System.out.println(i);
            i = i + 1;
        }
        System.out.println("------------ Inicio For -------------");
        for (int a = 1; a <= 10; a++) {
            System.out.println(a);
	}
          
    }
    
}
